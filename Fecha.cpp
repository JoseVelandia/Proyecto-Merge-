
#include "Fecha.h"


// CONSTRUCTORES
Fecha::Fecha(){
	
}

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

Fecha::Fecha(int Dia, int Mes, int Anio) {
	this->Dia = Dia ;
	this->Mes = Mes ;
	this->Anio = Anio ;
}

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

// SETTERS
void Fecha::setDia(int Dia) {
	this->Dia = Dia ;
}

void Fecha::setMes(int Mes) {
	this->Mes = Mes ;
}

void Fecha::setAnio(int Anio) {
	this->Anio = Anio ;
}

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

// GETTERS
int Fecha::getDia() {
	return this->Dia ;
}

int Fecha::getMes() {
	return this->Mes ;
}

int Fecha::getAnio() {
	return this->Anio ;
}

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

// DESTRUCTOR
Fecha::~Fecha() {
}
